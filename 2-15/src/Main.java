public class Main {
    public static void main(String[] args) {
        int score[] = {85, 90, 75, 95, 80};
        for (int i = 0; i < score.length; i++) {
            System.out.println((i + 1) + "番目の生徒：" + score[i] + "点");
        }
    }
}
//1番目の生徒：85点
//2番目の生徒：90点
//3番目の生徒：75点
//4番目の生徒：95点
//5番目の生徒：80点